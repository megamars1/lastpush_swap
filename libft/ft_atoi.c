#include"libft.h"

int	ft_atoi(const char *str)
{
	int			i;
	int			div;
	long int	r;

	i = 0;
	div = 1;
	r = 0;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\t' || str[i] == '\r'
		|| str[i] == '\f' || str[i] == '\v')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			div = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		r = (r * 10) + (str[i++] - 48);
	}
	return (r * div);
}
